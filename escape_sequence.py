# ESCAPE SEQUENCE
weather = 'it\'s sunny'
weather1 = "it's \"kind of\" sunny"
weather2 = "\t it's \"kind of\" sunny"  # the \t creates a tab space
weather3 = "\t it's \"kind of\" sunny \n hope you have a good day!"  # \n creates a new line
print(weather)
print(weather1)
print(weather2)
print(weather3)
